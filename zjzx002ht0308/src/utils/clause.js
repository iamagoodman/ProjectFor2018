<p class="pnode"><b><span>(六十)</span></b><b><span>&nbsp;</span></b><b><span><span>严重心脏衰竭</span>CRT心脏再同步治疗：</span></b><span><span>指因缺血性心脏病或扩张性心肌病导致慢性严重心脏衰竭，被保险人实际接受了</span>CRT治疗，以矫正心室收缩不协调和改善心脏功能。</span><b><span>接受治疗之前必须满足下列所有条件：</span></b></p>
